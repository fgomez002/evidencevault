// ---------- Timeline report (Phase 5) ----------
export function timelineReportHtml(incidents: Incident[], evidence: EvidenceFile[]): string {
  const items = [
    ...incidents.map((i) => ({ at: i.occurred_at, label: `Incident — ${i.title}`, sub: categoryMeta(i.category).label })),
    ...evidence.map((e) => ({
      at: e.captured_at,
      label: `Evidence — ${e.caption || e.original_filename || evidenceKindMeta(e.kind).label}`,
      sub: evidenceKindMeta(e.kind).label,
    })),
  ].sort((a, b) => new Date(b.at).getTime() - new Date(a.at).getTime());

  const rows = items
    .map((it) => `<tr><td>${fmt(it.at)}</td><td>${esc(it.label)}</td><td>${esc(it.sub)}</td></tr>`)
    .join('');
  const body = `<h2>Chronological timeline (${items.length} entries)</h2>
    <table><thead><tr><th>When</th><th>Event</th><th>Type</th></tr></thead>
    <tbody>${rows || '<tr><td colspan="3">No entries.</td></tr>'}</tbody></table>`;
  return shell('Timeline Report', 'Events and evidence in chronological order', body);
}

// ---------- Full case file (Phase 5) ----------
export interface CaseFileData {
  incidents: Incident[];
  evidence: EvidenceFile[];
  witnesses: Witness[];
  reports: PoliceReport[];
  logs: IntegrityLogRow[];
}

export function caseFileHtml(data: CaseFileData): string {
  const incidentCards = data.incidents
    .map(
      (i) => `<div class="card">
        <div class="title">${esc(i.title)}</div>
        <div class="row"><span class="label">Category</span><span>${esc(categoryMeta(i.category).label)}</span></div>
        <div class="row"><span class="label">When</span><span>${fmt(i.occurred_at)}</span></div>
        ${i.location_label ? `<div class="row"><span class="label">Location</span><span>${esc(i.location_label)}</span></div>` : ''}
        ${i.notes ? `<div class="row"><span class="label">Notes</span></div><div>${esc(i.notes)}</div>` : ''}
        ${i.emotional_impact ? `<div class="row"><span class="label">Impact</span></div><div>${esc(i.emotional_impact)}</div>` : ''}
        ${i.follow_up_actions ? `<div class="row"><span class="label">Follow-up</span></div><div>${esc(i.follow_up_actions)}</div>` : ''}
      </div>`,
    )
    .join('');

  const evidenceRows = data.evidence
    .map(
      (e) => `<tr><td>${esc(e.caption || e.original_filename || '—')}</td><td>${esc(evidenceKindMeta(e.kind).label)}</td>
        <td>${fmt(e.captured_at)}</td><td class="mono">${esc(e.sha256.slice(0, 24))}…</td></tr>`,
    )
    .join('');

  const witnessCards = data.witnesses
    .map(
      (w) => `<div class="card"><div class="title">${esc(w.name)}</div>
        <div class="row"><span class="label">Contact</span><span>${esc([w.phone, w.email].filter(Boolean).join(' · ')) || '—'}</span></div>
        ${w.written_statement ? `<div>${esc(w.written_statement)}</div>` : ''}</div>`,
    )
    .join('');

  const reportRows = data.reports
    .map(
      (r) => `<tr><td>${esc(r.report_number) || '—'}</td><td>${esc(r.agency) || '—'}</td>
        <td>${esc(r.filed_at) || '—'}</td><td>${esc(reportStatusMeta(r.status).label)}</td></tr>`,
    )
    .join('');

  const body = `
    <h2>Summary</h2>
    <table><tbody>
      <tr><td>Incidents</td><td>${data.incidents.length}</td></tr>
      <tr><td>Evidence items</td><td>${data.evidence.length}</td></tr>
      <tr><td>Witnesses</td><td>${data.witnesses.length}</td></tr>
      <tr><td>Police reports</td><td>${data.reports.length}</td></tr>
      <tr><td>Integrity events logged</td><td>${data.logs.length}</td></tr>
    </tbody></table>

    <h2>Incidents (${data.incidents.length})</h2>
    ${incidentCards || '<p>None.</p>'}

    <h2>Evidence (${data.evidence.length})</h2>
    <table><thead><tr><th>Description</th><th>Type</th><th>Captured</th><th>SHA-256</th></tr></thead>
    <tbody>${evidenceRows || '<tr><td colspan="4">None.</td></tr>'}</tbody></table>

    <h2>Witnesses (${data.witnesses.length})</h2>
    ${witnessCards || '<p>None.</p>'}

    <h2>Police reports (${data.reports.length})</h2>
    <table><thead><tr><th>Report #</th><th>Agency</th><th>Filed</th><th>Status</th></tr></thead>
    <tbody>${reportRows || '<tr><td colspan="4">None.</td></tr>'}</tbody></table>`;

  return shell('Case File', 'Complete record export', body);
}
