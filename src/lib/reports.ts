function shell(title: string, subtitle: string, body: string): string {
  return `<!DOCTYPE html><html><head><meta charset="utf-8">${STYLES}</head><body><div class="page">
    <div><span class="brand">🛡️ EvidenceVault</span></div>
    <h1>${esc(title)}</h1>
    <p class="sub">${esc(subtitle)}</p>
    <p class="meta">Generated ${new Date().toLocaleString()}</p>
    ${body}
    <div class="disclaimer">
      This document was generated by EvidenceVault from records maintained by the account holder.
      EvidenceVault is a documentation tool and does not verify the truth of recorded statements.
      File integrity hashes (SHA-256) are captured at the time each file is added to the vault.
    </div>
  </div></body></html>`;
}

// ---------- Integrity report (Phase 3) ----------
export function integrityReportHtml(evidence: EvidenceFile[], logs: IntegrityLogRow[]): string {
  const rows = evidence
    .map(
      (e) => `<tr>
        <td>${esc(e.caption || e.original_filename || evidenceKindMeta(e.kind).label)}</td>
        <td>${esc(evidenceKindMeta(e.kind).label)}</td>
        <td>${fmt(e.captured_at)}</td>
        <td class="mono">${esc(e.sha256)}</td>
      </tr>`,
    )
    .join('');

  const logRows = logs
    .slice(0, 500)
    .map(
      (l) => `<tr>
        <td>${fmt(l.occurred_at)}</td>
        <td>${esc(l.entity_type)}</td>
        <td>${esc(l.action)}</td>
        <td class="mono">${esc(l.sha256_after ?? l.sha256_before ?? '')}</td>
      </tr>`,
    )
    .join('');

  const body = `
    <h2>Evidence integrity (${evidence.length})</h2>
    <table><thead><tr><th>Item</th><th>Type</th><th>Captured</th><th>SHA-256</th></tr></thead>
      <tbody>${rows || '<tr><td colspan="4">No evidence.</td></tr>'}</tbody></table>
    <h2>Chain-of-custody log (${logs.length})</h2>
    <table><thead><tr><th>When</th><th>Entity</th><th>Action</th><th>Hash</th></tr></thead>
      <tbody>${logRows || '<tr><td colspan="4">No events.</td></tr>'}</tbody></table>`;
  return shell('Evidence Integrity Report', 'Digital chain of custody', body);
}

